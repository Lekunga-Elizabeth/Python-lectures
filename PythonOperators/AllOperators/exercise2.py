# Write a program that asks the user to enter two numbers
# compares them using the following comparison operators: `>`, `<`, `>=`, `<=`, `==`, and `!=`. 
# The program should print out the result of each comparison.

# get input from the user
num1=int(input("enter the first numbers:"))
num2=int(input("enter the second number:"))

# Perform the following operation and print the result in your terminal

print (num1 > num2)   # perform the greater than operation
print (num1 < num2)   # perform the less than operation
print (num1 >= num2)  # perform the greater than or equall to operation
print (num1 <= num2)  # perform the less than or equall to operation
print (num1 == num2)  # perform the equall to operation
print (num1 != num2)  # perform the not equal to operation

