#Electricity Bill for the 1st month = 23
first_bill = 23
print("your electricity bill for the 1st month is", first_bill, "dollars")

# Electricity Bill for the 2nd month = 32
second_bill = 32
print("your electricity bill for the 2nd month is", second_bill, "dollars")

# Electricity bill for the 3rd month = 64
third_bill = 64
print("your electrictiy bill for the 3rd month is", third_bill , "dollars")

total_bill = first_bill + second_bill + third_bill 
print("your total bill for 3 months is:", total_bill, "dollars")

average_bill = total_bill // 3
print("your monthly average electricity bill is:", average_bill, "dollars")