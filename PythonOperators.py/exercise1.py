# solution to the 1st exercise.
count=10
count +=5
print(count) # i added 5 to count and print the value of count to terminal 

count -=3
print(count) # i subtrated 3 from count and print the value of count to terminal 

count *=2
print(count) # i multiple 2 to count and print the value of the count to the terminal

